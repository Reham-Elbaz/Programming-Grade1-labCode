using System;

namespace Lab01
{
    class Program
    {
        static void Main(string[] args)
        {
            // To print some statements on multiple lines
            Console.WriteLine("I'm Reham.");
            Console.WriteLine("I'm 22 years old.");
            Console.WriteLine("I'm student at fcis.");


            // To print on a single line
            Console.Write("This will be");
            Console.Write("printed on the same line");


            // Printing on the same line using special character
            Console.Write("I'm Reham.\nI'm 22 years old.\nI'm student at fcis.");
        }
    }
}
